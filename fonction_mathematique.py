def perimetre_carre(cote: float):
    perimetre = cote * 4
    return perimetre


def aire_carre(cote: float):
    aire = cote * cote
    return aire


def perimetre_rectangle(longueur: float, largeur: float):
    perimetre = (longueur + largeur) * 2
    return perimetre


def aire_rectangle(longueur: float, largeur: float):
    aire = longueur * largeur
    return aire

